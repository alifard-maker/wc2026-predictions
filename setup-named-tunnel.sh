#!/bin/bash
# One-time setup: permanent public URL on your own domain (survives Mac restarts).
set -e
cd "$(dirname "$0")"

PORT="${PORT:-5050}"
BIN="./bin/cloudflared"
TUNNEL_NAME="${TUNNEL_NAME:-wc2026-predictions}"
CONFIG_PATH="data/cloudflare-tunnel.yml"
ENV_PATH="data/cloudflare-tunnel.env"

if [ ! -x "$BIN" ]; then
  echo "cloudflared not found — run ./share.sh once to download it, then re-run this script."
  exit 1
fi

if [ -z "$TUNNEL_HOSTNAME" ]; then
  echo ""
  echo "Permanent public URL setup"
  echo "=========================="
  echo ""
  echo "You need:"
  echo "  • A free Cloudflare account"
  echo "  • A domain on Cloudflare DNS (e.g. yourdomain.com)"
  echo ""
  read -r -p "Subdomain to use (e.g. wc.yourdomain.com): " TUNNEL_HOSTNAME
fi

if [ -z "$TUNNEL_HOSTNAME" ]; then
  echo "ERROR: hostname is required."
  exit 1
fi

PUBLIC_URL="https://${TUNNEL_HOSTNAME}"

echo ""
echo "Step 1/4 — Log in to Cloudflare (browser will open)..."
echo "Pick the zone that contains: ${TUNNEL_HOSTNAME#*.}"
"$BIN" tunnel login

echo ""
echo "Step 2/4 — Create named tunnel: ${TUNNEL_NAME}"
if "$BIN" tunnel list 2>/dev/null | grep -q "$TUNNEL_NAME"; then
  echo "Tunnel already exists, reusing it."
else
  "$BIN" tunnel create "$TUNNEL_NAME"
fi

TUNNEL_ID=$("$BIN" tunnel list 2>/dev/null | awk -v name="$TUNNEL_NAME" '$0 ~ name {print $1; exit}')
if [ -z "$TUNNEL_ID" ]; then
  echo "ERROR: could not read tunnel ID. Run: $BIN tunnel list"
  exit 1
fi

CREDS="$HOME/.cloudflared/${TUNNEL_ID}.json"
if [ ! -f "$CREDS" ]; then
  echo "ERROR: credentials file not found at $CREDS"
  exit 1
fi

echo ""
echo "Step 3/4 — DNS route: ${TUNNEL_HOSTNAME} → tunnel"
"$BIN" tunnel route dns "$TUNNEL_NAME" "$TUNNEL_HOSTNAME" || true

mkdir -p data
cat > "$CONFIG_PATH" <<EOF
# Generated by setup-named-tunnel.sh — do not commit credentials.
tunnel: ${TUNNEL_ID}
credentials-file: ${CREDS}
protocol: http2

ingress:
  - hostname: ${TUNNEL_HOSTNAME}
    service: http://localhost:${PORT}
  - service: http_status:404
EOF

cat > "$ENV_PATH" <<EOF
# Permanent public URL for invite links. Sourced by start.sh / share-persistent.sh
PUBLIC_URL=${PUBLIC_URL}
TUNNEL_HOSTNAME=${TUNNEL_HOSTNAME}
TUNNEL_NAME=${TUNNEL_NAME}
EOF

echo ""
echo "Step 4/4 — Done."
echo ""
echo "  Permanent URL: ${PUBLIC_URL}"
echo "  Config:        ${CONFIG_PATH}"
echo ""
echo "Start sharing:"
echo "  Terminal 1:  ./start.sh"
echo "  Terminal 2:  ./share-persistent.sh"
echo ""
echo "After a Mac restart, run the same two commands — the URL stays ${PUBLIC_URL}"
echo "Copy a fresh invite link from Guide (it will use the permanent URL)."
